using System;

class CharType{
    static void Main(){
        char ch = 'a';
        Console.WriteLine("The code of '"+ch+"' is "+(int)ch);
        ch = 'b';
        Console.WriteLine("The code of '"+ch+"' is "+(int)ch);
        ch = 'A';
        Console.WriteLine("The code of '"+ch+"' is "+(int)ch);
    }
}